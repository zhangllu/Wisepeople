/**
 * Build-time script: reads 智者资料库 markdown files and generates
 * src/data/wise-content.json so the content is bundled at build time
 * instead of being read from the filesystem at runtime.
 *
 * This avoids Vercel serverless function path resolution issues.
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const PROJECT_ROOT = path.resolve(__dirname, '..')
const WISE_PEOPLE_DIR = path.join(PROJECT_ROOT, '智者资料库')
const OUTPUT_PATH = path.join(PROJECT_ROOT, 'src', 'data', 'wise-content.ts')

const METADATA_PATH = path.join(WISE_PEOPLE_DIR, 'metadata.json')
if (!fs.existsSync(METADATA_PATH)) {
  console.error('metadata.json not found at', METADATA_PATH)
  process.exit(1)
}

const metadata = JSON.parse(fs.readFileSync(METADATA_PATH, 'utf-8'))
const wisePeople = metadata.wise_people || []

/** Safely embed a raw string as a JS string literal using JSON.stringify */
function embed(str) {
  return JSON.stringify(str)
}

let output = `// Auto-generated by scripts/compile-wise-content.mjs — DO NOT EDIT\n`
output += `export interface WiseContentEntry {\n`
output += `  introduction: string | null;\n`
output += `  basicInfo: string | null;\n`
output += `  cognitiveStyle: string | null;\n`
output += `}\n\n`
output += `export const wiseContent: Record<string, WiseContentEntry> = {\n`

let count = 0
for (const person of wisePeople) {
  const { slug } = person
  const basePath = path.join(WISE_PEOPLE_DIR, slug)

  if (!fs.existsSync(basePath)) {
    console.warn(`[warn] Directory not found for ${slug}, skipping`)
    continue
  }

  const entry = { introduction: null, basicInfo: null, cognitiveStyle: null }

  for (const [key, filename] of [
    ['introduction', '01-智者介绍.md'],
    ['basicInfo', '02-基本信息.md'],
    ['cognitiveStyle', '03-认知方式.md'],
  ]) {
    const filePath = path.join(basePath, filename)
    if (fs.existsSync(filePath)) {
      entry[key] = fs.readFileSync(filePath, 'utf-8')
    }
  }

  output += `  ${embed(slug)}: {\n`
  output += `    introduction: ${embed(entry.introduction)},\n`
  output += `    basicInfo: ${embed(entry.basicInfo)},\n`
  output += `    cognitiveStyle: ${embed(entry.cognitiveStyle)},\n`
  output += `  },\n`
  count++
}

output += `};\n`

fs.writeFileSync(OUTPUT_PATH, output, 'utf-8')
console.log(`✓ Generated wise-content.ts with ${count} entries → ${OUTPUT_PATH}`)
